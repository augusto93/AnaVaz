let modeIndex = 1;

// slide 01
modeSlides1(modeIndex);

function changeAp01(n) {
  modeSlides1(modeIndex = n);
}

function modeSlides1(n) {
  let i;
	let modal = document.getElementsByClassName("ap01");

  if (n > modal.length) {modeIndex = 1}
  if (n < 1) {modeIndex = modal.length}
  for (i = 0; i < modal.length; i++) {
    modal[i].style.display = "none";
  }
  modal[modeIndex-1].style.display = "flex";
}

